/**
 **_strchr - locates a char in a string
 *@s: string to be parsed
 *@c: char to look for
 *Return: a pointer to the located char
 */
char *_strchr(char *s, char c)
{
	do {
		if (*s == c)
			return (s);
	} while (*s++ != '\0');

	return (NULL);
}

